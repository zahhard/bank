package com.example.bank;

public class Person {

    String fullName ;
    String birthPlace ;
    String nationalCode ;
    String postCode ;
    String gender ;

    Person ( String inputFullName ,  String inputBirthPlace , String inputNationalCode  , String inputPostCode , String inputGender){
        fullName = inputFullName ;
        birthPlace = inputBirthPlace ;
        nationalCode = inputNationalCode ;
        postCode = inputPostCode ;
        gender = inputGender ;
    }
}
